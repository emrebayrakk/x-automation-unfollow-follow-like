// Sunucu tarafı çeviri: t(lang, 'log', anahtar, {param}) — burada namespace her zaman 'log'.
export function t(lang, key, params = {}) {
  const L = (locales[lang] && locales[lang].log) ? lang : 'tr';
  let s = locales[L].log[key] ?? locales.tr.log[key] ?? key;
  for (const k in params) s = s.split('{' + k + '}').join(String(params[k]));
  return s;
}
